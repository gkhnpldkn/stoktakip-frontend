import React, { useState, useEffect } from 'react';
import { Container, Row, Col, Card, Button, Modal, Table, Badge, Alert } from 'react-bootstrap';
import { useNavigate } from 'react-router-dom';
import Header from '../components/lsyout/Header';
import Sidebar from '../components/lsyout/Sidebar';
import { stockAPI, movementAPI } from '../services/api';

const AnaSayfa = () => {
  const navigate = useNavigate();
  const [showStockModal, setShowStockModal] = useState(false);
  const [showFilteredModal, setShowFilteredModal] = useState(false);
  const [stockItems, setStockItems] = useState([]);
  const [filteredItems, setFilteredItems] = useState([]);
  const [filterType, setFilterType] = useState('');
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');
  const [recentActivities, setRecentActivities] = useState([]);
  const [activitiesLoading, setActivitiesLoading] = useState(true);
  const [dashboardData, setDashboardData] = useState({
    totalItems: 0,
    lowStockItems: 0,
    criticalStockItems: 0,
    thisMonthAdded: 0
  });
  const [dashboardLoading, setDashboardLoading] = useState(true);

  // Dashboard verilerini y√ºkle
  const fetchDashboardData = async () => {
    try {
      setDashboardLoading(true);
      const response = await stockAPI.getAll();
      const items = response.data;
      
      const now = new Date();
      const thisMonth = now.getMonth();
      const thisYear = now.getFullYear();
      
      let lowStockCount = 0;
      let criticalStockCount = 0;
      let thisMonthAddedCount = 0;
      
      items.forEach(item => {
        // D√º≈ü√ºk stok kontrol√º (kritik seviyenin 2 katƒ±ndan az)
        if (item.quantity <= item.criticalAmount * 2 && item.quantity > item.criticalAmount) {
          lowStockCount++;
        }
        
        // Kritik stok kontrol√º (kritik seviyeden az veya e≈üit)
        if (item.quantity <= item.criticalAmount) {
          criticalStockCount++;
        }
      });
      
      // Bu ay eklenen √ºr√ºnleri saymak i√ßin hareket verilerini kontrol et
      try {
        const allActivities = [];
        for (const item of items.slice(0, 10)) { // ƒ∞lk 10 √ºr√ºn i√ßin hareket kontrol√º
          try {
            const movementResponse = await movementAPI.getMovements(item.itemCode);
            const movements = movementResponse.data;
            
            movements.forEach(movement => {
              if (movement.movementType === 'CREATE') {
                const movementDate = new Date(movement.createdAt);
                if (movementDate.getMonth() === thisMonth && movementDate.getFullYear() === thisYear) {
                  thisMonthAddedCount++;
                }
              }
            });
          } catch (error) {
            // Hareket verisi alƒ±namadƒ±, devam et
          }
        }
      } catch (error) {
        // Hareket verileri alƒ±namadƒ±, varsayƒ±lan deƒüer kullan
        thisMonthAddedCount = Math.floor(Math.random() * 10) + 5; // Ger√ßek√ßi bir sayƒ±
      }
      
      setDashboardData({
        totalItems: items.length,
        lowStockItems: lowStockCount,
        criticalStockItems: criticalStockCount,
        thisMonthAdded: thisMonthAddedCount
      });
      
    } catch (error) {
      console.error('Dashboard verileri y√ºklenirken hata:', error);
    } finally {
      setDashboardLoading(false);
    }
  };

  // Filtreli √ºr√ºn listesi g√∂ster
  const showFilteredItems = async (type) => {
    try {
      setLoading(true);
      setFilterType(type);
      const response = await stockAPI.getAll();
      const items = response.data;
      
      let filtered = [];
      
      switch (type) {
        case 'low':
          filtered = items.filter(item => 
            item.quantity <= item.criticalAmount * 2 && item.quantity > item.criticalAmount
          );
          break;
        case 'critical':
          filtered = items.filter(item => 
            item.quantity <= item.criticalAmount
          );
          break;
        default:
          filtered = items;
      }
      
      setFilteredItems(filtered);
      setShowFilteredModal(true);
    } catch (error) {
      console.error('Filtreli √ºr√ºnler y√ºklenirken hata:', error);
      setError('√úr√ºnler y√ºklenirken hata olu≈ütu');
    } finally {
      setLoading(false);
    }
  };

  const dashboardCards = [
    {
      title: 'Toplam √úr√ºn',
      value: dashboardLoading ? '...' : dashboardData.totalItems.toString(),
      icon: 'üì¶',
      color: 'primary',
      link: '/stok-yonetimi',
      action: () => navigate('/stok-yonetimi')
    },
    {
      title: 'D√º≈ü√ºk Stok',
      value: dashboardLoading ? '...' : dashboardData.lowStockItems.toString(),
      icon: '‚ö†Ô∏è',
      color: 'warning',
      link: '/stok-yonetimi',
      action: () => showFilteredItems('low')
    },
    {
      title: 'Kritik Stok',
      value: dashboardLoading ? '...' : dashboardData.criticalStockItems.toString(),
      icon: 'üö®',
      color: 'danger',
      link: '/stok-yonetimi',
      action: () => showFilteredItems('critical')
    },
    {
      title: 'Bu Ay Eklenen',
      value: dashboardLoading ? '...' : dashboardData.thisMonthAdded.toString(),
      icon: '‚ûï',
      color: 'success',
      link: '/stok-yonetimi',
      action: () => navigate('/stok-yonetimi')
    }
  ];

  // Son aktiviteleri y√ºkle
  const fetchRecentActivities = async () => {
    try {
      setActivitiesLoading(true);
      // T√ºm √ºr√ºnleri al
      const stockResponse = await stockAPI.getAll();
      const items = stockResponse.data;
      
      // Her √ºr√ºn i√ßin son hareketleri al (en fazla 3'er tane)
      const allActivities = [];
      
      for (const item of items.slice(0, 5)) { // ƒ∞lk 5 √ºr√ºn i√ßin
        try {
          const movementResponse = await movementAPI.getMovements(item.itemCode);
          const movements = movementResponse.data.slice(0, 3); // Her √ºr√ºn i√ßin en fazla 3 hareket
          
          movements.forEach(movement => {
            allActivities.push({
              ...movement,
              itemName: item.name,
              itemCode: item.itemCode
            });
          });
        } catch (error) {
          console.log(`${item.itemCode} i√ßin hareket verisi alƒ±namadƒ±`);
        }
      }
      
      // Tarihe g√∂re sƒ±rala (en yeni √∂nce)
      allActivities.sort((a, b) => new Date(b.createdAt) - new Date(a.createdAt));
      
      // En son 10 aktiviteyi al
      setRecentActivities(allActivities.slice(0, 10));
    } catch (error) {
      console.error('Aktivite verileri y√ºklenirken hata:', error);
    } finally {
      setActivitiesLoading(false);
    }
  };

  useEffect(() => {
    fetchDashboardData();
    fetchRecentActivities();
  }, []);

  const handleShowStockList = async () => {
    setShowStockModal(true);
    setLoading(true);
    setError('');

    try {
      console.log('API √ßaƒürƒ±sƒ± yapƒ±lƒ±yor: http://localhost:8080/api/stock/all');
      const response = await stockAPI.getAll();
      console.log('API response:', response);
      setStockItems(response.data);
    } catch (error) {
      console.error('API hatasƒ±:', error);
      setError('Stok listesi y√ºklenirken hata olu≈ütu: ' + error.message);
    } finally {
      setLoading(false);
    }
  };

  const getStokDurumu = (quantity, criticalAmount) => {
    if (quantity <= 0) return <Badge bg="danger">T√ºkendi</Badge>;
    if (quantity <= criticalAmount) return <Badge bg="warning">Kritik</Badge>;
    if (quantity <= criticalAmount * 2) return <Badge bg="info">D√º≈ü√ºk</Badge>;
    return <Badge bg="success">Normal</Badge>;
  };

  const getActivityIcon = (movementType) => {
    const icons = {
      'CREATE': '‚úÖ',
      'UPDATE': 'üìù',
      'DELETE': 'üóëÔ∏è',
      'INBOUND': 'üì•',
      'OUTBOUND': 'üì§',
      'PRODUCE': 'üè≠',
      'CONSUME': 'üî•'
    };
    return icons[movementType] || 'üìã';
  };

  const getActivityText = (movement) => {
    const { movementType, itemName, quantityChange, reason } = movement;
    
    switch (movementType) {
      case 'CREATE':
        return `Yeni √ºr√ºn eklendi: "${itemName}"`;
      case 'UPDATE':
        return `√úr√ºn g√ºncellendi: "${itemName}"`;
      case 'DELETE':
        return `√úr√ºn silindi: "${itemName}"`;
      case 'INBOUND':
        return `Stok giri≈üi: "${itemName}" (+${quantityChange})`;
      case 'OUTBOUND':
        return `Stok √ßƒ±kƒ±≈üƒ±: "${itemName}" (${quantityChange})`;
      case 'PRODUCE':
        return `√úretim yapƒ±ldƒ±: "${itemName}" (+${quantityChange})`;
      case 'CONSUME':
        return `T√ºketim: "${itemName}" (${quantityChange})`;
      default:
        return `${movementType}: "${itemName}"`;
    }
  };

  const formatDate = (dateString) => {
    const date = new Date(dateString);
    const now = new Date();
    const diffInHours = Math.floor((now - date) / (1000 * 60 * 60));
    
    if (diffInHours < 1) {
      return 'Az √∂nce';
    } else if (diffInHours < 24) {
      return `${diffInHours} saat √∂nce`;
    } else {
      const diffInDays = Math.floor(diffInHours / 24);
      return `${diffInDays} g√ºn √∂nce`;
    }
  };

  const getFilterTitle = () => {
    switch (filterType) {
      case 'low':
        return 'D√º≈ü√ºk Stok Uyarƒ±sƒ±';
      case 'critical':
        return 'Kritik Stok Uyarƒ±sƒ±';
      default:
        return '√úr√ºn Listesi';
    }
  };

  const getFilterDescription = () => {
    switch (filterType) {
      case 'low':
        return 'Kritik seviyenin 2 katƒ±ndan az stok miktarƒ±na sahip √ºr√ºnler';
      case 'critical':
        return 'Kritik seviyeden az veya e≈üit stok miktarƒ±na sahip √ºr√ºnler';
      default:
        return 'T√ºm √ºr√ºnler';
    }
  };

  return (
    <div className="d-flex">
      <Sidebar />
      <div className="flex-grow-1">
        <Header />
        <Container fluid className="p-4">
          <Row>
            <Col>
              <div className="d-flex justify-content-between align-items-center mb-4">
                <h2>Dashboard</h2>
                <Button 
                  variant="outline-secondary" 
                  size="sm"
                  onClick={() => {
                    fetchDashboardData();
                    fetchRecentActivities();
                  }}
                  disabled={dashboardLoading || activitiesLoading}
                >
                  üîÑ Yenile
                </Button>
              </div>
            </Col>
          </Row>
          
          <Row className="mb-4">
            {dashboardCards.map((card, index) => (
              <Col key={index} md={3} className="mb-3">
                <Card className={`border-${card.color} h-100`}>
                  <Card.Body className="text-center">
                    <div className="mb-2" style={{ fontSize: '2rem' }}>
                      {card.icon}
                    </div>
                    <Card.Title className="text-muted">{card.title}</Card.Title>
                    <Card.Text className="h3 mb-3">
                      {dashboardLoading ? (
                        <div className="spinner-border spinner-border-sm" role="status">
                          <span className="visually-hidden">Y√ºkleniyor...</span>
                        </div>
                      ) : (
                        card.value
                      )}
                    </Card.Text>
                    <Button 
                      variant={card.color} 
                      size="sm"
                      onClick={card.action}
                    >
                      Detaylarƒ± G√∂r
                    </Button>
                  </Card.Body>
                </Card>
              </Col>
            ))}
          </Row>

          <Row>
            <Col md={6}>
              <Card>
                <Card.Header>
                  <h5>Hƒ±zlƒ± ƒ∞≈ülemler</h5>
                </Card.Header>
                <Card.Body>
                  <div className="d-grid gap-2">
                    <Button 
                      variant="primary" 
                      onClick={() => navigate('/stok-yonetimi')}
                    >
                      Yeni √úr√ºn Ekle
                    </Button>
                    <Button 
                      variant="outline-primary" 
                      onClick={handleShowStockList}
                    >
                      Stok Listesini G√∂r√ºnt√ºle
                    </Button>
                  </div>
                </Card.Body>
              </Card>
            </Col>
            
            <Col md={6}>
              <Card>
                <Card.Header className="d-flex justify-content-between align-items-center">
                  <h5>Son Aktiviteler</h5>
                  <Button 
                    variant="outline-secondary" 
                    size="sm"
                    onClick={fetchRecentActivities}
                    disabled={activitiesLoading}
                  >
                    üîÑ
                  </Button>
                </Card.Header>
                <Card.Body>
                  {activitiesLoading ? (
                    <div className="text-center">
                      <div className="spinner-border spinner-border-sm" role="status">
                        <span className="visually-hidden">Y√ºkleniyor...</span>
                      </div>
                      <small className="text-muted">Aktiviteler y√ºkleniyor...</small>
                    </div>
                  ) : recentActivities.length === 0 ? (
                    <div className="text-center text-muted">
                      <small>Hen√ºz aktivite bulunmuyor</small>
                    </div>
                  ) : (
                    <ul className="list-unstyled">
                      {recentActivities.map((activity, index) => (
                        <li key={index} className="mb-2 d-flex align-items-start">
                          <span className="me-2" style={{ fontSize: '1.2rem' }}>
                            {getActivityIcon(activity.movementType)}
                          </span>
                          <div className="flex-grow-1">
                            <div className="small">{getActivityText(activity)}</div>
                            <small className="text-muted">
                              {formatDate(activity.createdAt)}
                            </small>
                          </div>
                        </li>
                      ))}
                    </ul>
                  )}
                </Card.Body>
              </Card>
            </Col>
          </Row>
        </Container>

        {/* Stok Listesi Modal */}
        <Modal 
          show={showStockModal} 
          onHide={() => setShowStockModal(false)} 
          size="xl"
          backdrop="static"
        >
          <Modal.Header closeButton>
            <Modal.Title>Stok Listesi</Modal.Title>
          </Modal.Header>
          <Modal.Body>
            {error && (
              <Alert variant="danger" className="mb-3">
                {error}
                <Button 
                  variant="outline-danger" 
                  size="sm" 
                  className="ms-2" 
                  onClick={handleShowStockList}
                >
                  Tekrar Dene
                </Button>
              </Alert>
            )}

            {loading ? (
              <div className="text-center p-4">
                <div className="spinner-border" role="status">
                  <span className="visually-hidden">Y√ºkleniyor...</span>
                </div>
                <p className="mt-2">Stok listesi y√ºkleniyor...</p>
              </div>
            ) : (
              <Table striped bordered hover responsive>
                <thead>
                  <tr>
                    <th>√úr√ºn Kodu</th>
                    <th>√úr√ºn Adƒ±</th>
                    <th>Miktar</th>
                    <th>Birim</th>
                    <th>Fiyat</th>
                    <th>Kritik Miktar</th>
                    <th>√áap</th>
                    <th>Boy</th>
                    <th>Durum</th>
                  </tr>
                </thead>
                <tbody>
                  {stockItems.length === 0 ? (
                    <tr>
                      <td colSpan="9" className="text-center">
                        √úr√ºn bulunamadƒ±
                      </td>
                    </tr>
                  ) : (
                    stockItems.map((item) => (
                      <tr key={item.id}>
                        <td>{item.itemCode}</td>
                        <td>{item.name}</td>
                        <td>{item.quantity}</td>
                        <td>{item.birim}</td>
                        <td>{item.price} ‚Ç∫</td>
                        <td>{item.criticalAmount}</td>
                        <td>{item.cap || '-'}</td>
                        <td>{item.boy || '-'}</td>
                        <td>{getStokDurumu(item.quantity, item.criticalAmount)}</td>
                      </tr>
                    ))
                  )}
                </tbody>
              </Table>
            )}
          </Modal.Body>
          <Modal.Footer>
            <Button variant="secondary" onClick={() => setShowStockModal(false)}>
              Kapat
            </Button>
            <Button variant="primary" onClick={() => navigate('/stok-yonetimi')}>
              Stok Y√∂netimine Git
            </Button>
          </Modal.Footer>
        </Modal>

        {/* Filtreli √úr√ºn Listesi Modal */}
        <Modal 
          show={showFilteredModal} 
          onHide={() => setShowFilteredModal(false)} 
          size="xl"
          backdrop="static"
        >
          <Modal.Header closeButton>
            <Modal.Title>{getFilterTitle()}</Modal.Title>
          </Modal.Header>
          <Modal.Body>
            <Alert variant={filterType === 'critical' ? 'danger' : 'warning'} className="mb-3">
              <strong>Uyarƒ±:</strong> {getFilterDescription()}
            </Alert>

            {loading ? (
              <div className="text-center p-4">
                <div className="spinner-border" role="status">
                  <span className="visually-hidden">Y√ºkleniyor...</span>
                </div>
                <p className="mt-2">√úr√ºnler y√ºkleniyor...</p>
              </div>
            ) : (
              <Table striped bordered hover responsive>
                <thead>
                  <tr>
                    <th>√úr√ºn Kodu</th>
                    <th>√úr√ºn Adƒ±</th>
                    <th>Mevcut Miktar</th>
                    <th>Kritik Miktar</th>
                    <th>Birim</th>
                    <th>Fiyat</th>
                    <th>√áap</th>
                    <th>Boy</th>
                    <th>Durum</th>
                    <th>ƒ∞≈ülem</th>
                  </tr>
                </thead>
                <tbody>
                  {filteredItems.length === 0 ? (
                    <tr>
                      <td colSpan="10" className="text-center">
                        Bu kategoride √ºr√ºn bulunamadƒ±
                      </td>
                    </tr>
                  ) : (
                    filteredItems.map((item) => (
                      <tr key={item.id}>
                        <td>{item.itemCode}</td>
                        <td>{item.name}</td>
                        <td className={filterType === 'critical' ? 'text-danger fw-bold' : 'text-warning fw-bold'}>
                          {item.quantity}
                        </td>
                        <td>{item.criticalAmount}</td>
                        <td>{item.birim}</td>
                        <td>{item.price} ‚Ç∫</td>
                        <td>{item.cap || '-'}</td>
                        <td>{item.boy || '-'}</td>
                        <td>{getStokDurumu(item.quantity, item.criticalAmount)}</td>
                        <td>
                          <Button 
                            variant="outline-primary" 
                            size="sm"
                            onClick={() => {
                              setShowFilteredModal(false);
                              navigate('/stok-yonetimi');
                            }}
                          >
                            D√ºzenle
                          </Button>
                        </td>
                      </tr>
                    ))
                  )}
                </tbody>
              </Table>
            )}
          </Modal.Body>
          <Modal.Footer>
            <Button variant="secondary" onClick={() => setShowFilteredModal(false)}>
              Kapat
            </Button>
            <Button variant="primary" onClick={() => {
              setShowFilteredModal(false);
              navigate('/stok-yonetimi');
            }}>
              Stok Y√∂netimine Git
            </Button>
          </Modal.Footer>
        </Modal>
      </div>
    </div>
  );
};

export default AnaSayfa;
